    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed
        int a1, a2;
        a1 = jTextFieldUsername.getText().length();
        a2 = String.valueOf(jPasswordFieldPass.getPassword()).length();
        String username = jTextFieldUsername.getText();
        String password = String.valueOf(jPasswordFieldPass.getPassword());

        if (a1 == 0) {
            JOptionPane.showMessageDialog(this, "Please enter your username", "WARNING", JOptionPane.WARNING_MESSAGE);
        } else if (a2 == 0) {
            JOptionPane.showMessageDialog(this, "Please enter your password", "WARNING", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                var conn = HyperMethod.getConnection();
                var pst = conn.prepareStatement("select user_id from users where "
                        + "user_username = ? and "
                        + "user_password = ?");
                pst.setString(1, username);
                pst.setString(2, password);
                var rs = pst.executeQuery();
                if (!rs.next()) {
                    JOptionPane.showMessageDialog(this, "Username or password is wrong", "ERROR", JOptionPane.ERROR_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Login Successful.\n"
                            + "Redirecting to Dashboard.", "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
                    
                    Dashboard  ds = new Dashboard();
                    ds.setVisible(true);
                    this.dispose();
                }
            } catch (SQLException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButtonLoginActionPerformed

