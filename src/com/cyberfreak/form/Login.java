    private boolean isValidCharForUsername(java.awt.event.KeyEvent evt) {

        if (!Character.isLetter(evt.getKeyChar()) && !Character.isDigit(evt.getKeyChar()) && !(evt.getKeyChar() == '_')) {
            return false;
        } else if (jTextFieldUsername.getText().length() > 23) {
            return false;
        }
        return true;
    }
